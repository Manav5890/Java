class Narrowing{
	public static void main(String[] args){


	// double into float
	double d = 78.6;
	float f = (float) d;
	System.out.println(f);

	//double into long
	double a = 78.3;
	long a1 = (long) a;
	System.out.println(a1);	
	
	//double into int
	double b = 52.3;
	int b1 = (int) b;
	System.out.println(b1);
	
	//double into short
	double c = 56.33;
	short c1 = (short) c;
	System.out.println(c1);
	
	// double into byte
	double e = 12.2;
	byte e1 = (byte) e;
	System.out.println(e1);

	//float to long
	float f1 = 12.5f;
	long f2 = (long) f1;
	System.out.println(f2);

	//float to int
	float h1 = 22.12f;
	int h2 = (int) h1;
	System.out.println(h2);

	// float to short
	float k1 = 52.4f;
	short k2 = (short) k1;
	System.out.println(k2);

	//float to byte
	float l1= 125.3f;
	byte l2 = (byte) l1;
	System.out.println(l2);


	//long to int
	long l3 = 1561;
	int l4 = (int) l3;
	System.out.println(l4);

	//long to short
	long l5 = 155615;
	short l6 = (short) l5;
	System.out.println(l6);

	//long to byte	
	long l7 = 23315;
	byte l8 = (byte) l7;
	System.out.println(l8);

	//int to byte
	int i4 = 12;
	byte i5 = (byte) i4;
	System.out.println(i5);
	
	//int to short
	int i6 = 12;
	short i7 = (short) i6;
	System.out.println(i7);
	
	// double to char
	double d5 = 78;
	char ch = (char) d5;
	System.out.println(ch);

	//float to char
	float f0 = 48.2f;
	char ch1 = (char) f0;
	System.out.println(ch1);





}




}
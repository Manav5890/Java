class widening{

	public static void main(String[] args){
		
	
	//byte to short
				
	byte b = 70;
	short a =b;
	System.out.println(a);
	
	//short to int

	short s = 58;
	int d = s;
	System.out.println(d);

	//int to long
	
	int i = 88;
	long f = i;
	System.out.println(f);

	
	// long to float
	long l = 80;
	float k = l;
	System.out.println(k);

	//float to double
	
	float m = 70.5f;
	double w = m;
	System.out.println(w);

	// char to int
	char n = 'D';
	int p = a;
	System.out.println(p);

	int o = 1282;
	System.out.println(o);

	// narrowing
	
	int r = 128;
	byte z = (byte) r;
	System.out.println(z);



}






}